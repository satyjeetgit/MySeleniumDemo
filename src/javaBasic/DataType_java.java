package javaBasic;

public class DataType_java {

	public static void main(String[] args) {
		//		 We have two main type of datatype
		//		Primitive Datatype and Non-primitive data type
		//		Primitive includes int, double, char and boolean
		//		Non- primitive includes String

		int a= 10;
		double b= 1.2;
		char c= 'A';

		String d = "Hello Data Type ";
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);


	}

}
