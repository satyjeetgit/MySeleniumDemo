package javaBasic;

public class If_Else_If_Demo {

	public static void main(String[] args) {
		//this are local variable
		int x= 20;
		if(x>30)
		{
			System.out.println("If code executed");	
		}
		else if(x==20)
		{
			System.out.println("Else If code is executed");
		}

		else
		{
			System.out.println(" Else code is executed");
		}
	}

}
